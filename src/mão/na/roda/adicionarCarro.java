    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String TIPO = jcTipo.getSelectedItem().toString();

        if (TIPO.equals("Aluguel")) {
            try {

                Aluguel al = new Aluguel(c_modelo.getText(), Integer.parseInt(c_ano.getText()), c_marca.getText(), c_cor.getText(), c_placa.getText(), c_categoria.getText(), jcGrupo.getSelectedItem().toString(), 7000);
                cAluguel.addAluguel(al);
                cCarro.addCarro(al);

                JOptionPane.showMessageDialog(
                        null,
                        "Veículo adicionado com sucesso",
                        "Veículo adicionado",
                        JOptionPane.INFORMATION_MESSAGE);

                c_modelo.setText("");
                c_ano.setText("");
                c_marca.setText("");
                c_cor.setText("");
                c_placa.setText("");
                c_categoria.setText("");

            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(
                        null,
                        "Erro ao adicionar veículo",
                        "Erro",
                        JOptionPane.ERROR_MESSAGE);

                c_modelo.setText("");
                c_ano.setText("");
                c_marca.setText("");
                c_cor.setText("");
                c_placa.setText("");
                c_categoria.setText("");
            }
        } else {
            try {
                Venda v = new Venda(c_modelo.getText(), Integer.parseInt(c_ano.getText()), c_marca.getText(), c_cor.getText(), c_placa.getText(), c_categoria.getText(), 70000);

                cVenda.addVenda(v);
                cCarro.addCarro(v);
                JOptionPane.showMessageDialog(
                        null,
                        "Veículo adicionado com sucesso",
                        "Veículo adicionado",
                        JOptionPane.INFORMATION_MESSAGE);

                c_modelo.setText("");
                c_ano.setText("");
                c_marca.setText("");
                c_cor.setText("");
                c_placa.setText("");
                c_categoria.setText("");

            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(
                        null,
                        "Erro ao adicionar veículo",
                        "Erro",
                        JOptionPane.ERROR_MESSAGE);

                c_modelo.setText("");
                c_ano.setText("");
                c_marca.setText("");
                c_cor.setText("");
                c_placa.setText("");
                c_categoria.setText("");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

