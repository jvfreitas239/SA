    private void jcTipoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcTipoItemStateChanged
        if (evt.getStateChange() == 2) {
            if (jcTipo.getSelectedItem().toString().equals("Aluguel")) {
                jcGrupo.setVisible(true);
                l_grupo.setVisible(true);
                jlValor.setVisible(false);
                jtValor.setVisible(false);

                return;
            }
            jcGrupo.setVisible(false);
            l_grupo.setVisible(false);
            jlValor.setVisible(false);
            jtValor.setVisible(false);
        }

        if (evt.getStateChange() == 2) {
            if (jcTipo.getSelectedItem().toString().equals("Venda")) {
                jlValor.setVisible(true);
                jtValor.setVisible(true);
                jcGrupo.setVisible(false);
                l_grupo.setVisible(false);

                return;
            }
            jcGrupo.setVisible(false);
            l_grupo.setVisible(false);
            jlValor.setVisible(false);
            jtValor.setVisible(false);
        }


    }//GEN-LAST:event_jcTipoItemStateChanged

